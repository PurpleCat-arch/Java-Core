

public class Main {
    public static void main(String[] args) {
        System.out.println("----FOR LOOP----");
        for(int i=1;i<=5;i++){
            System.out.println(i+" ");
        }

        System.out.println("----WHILE LOOP----");
        int j=1;
        while(j<=5){
            System.out.println(j+" ");
            j++;
        }

        System.out.println("----DO-WHILE LOOP----");
        int k=1;
        do { 
            System.out.println(k+" ");
            k++;
        } while (k<=5);
    }
    
}
